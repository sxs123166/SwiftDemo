// 面向对象概述
// 从整体的功能上看Swift的枚举、结构体、类三者具有完全平等的地位
// Swift的类、结构体、枚举中都可以定义（属性、方法、下表、构造体、嵌套类型）
// 在swift中，枚举和结构体是值类型的，类是引用类型

// 类和结构体相似点
// 定义属性用来存储值
// 定义方法用于提供功能
// 定义下标脚本用来允许使用下标语法访问值
// 定义初始化器用于初始化状态
// 可以被扩展来默认所没有的功能
// 遵循协议来针对特定类型提供标准功能

// 类和结构体不同点
// 继承允许一个类继承另一个类的特征
// 类型转换允许你在运行检查和解释一个类实例的类型
// 反初始化器允许一个类实例释放任何其所被分配的资源
// 引用计数允许不止一个对类实例的引用
